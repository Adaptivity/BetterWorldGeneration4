	public void generate(IChunkProvider ichunkprovider, World world, int chunkX, int chunkY, byte blocks[])
    {
        int x = chunkX * 16;
        int z = chunkY * 16;

    	for(int i = 0; i < 16; i++)
    	{
    		for(int j = 0; j < 16; j++)
    		{
    			int b = getTileBlock(chunkX, chunkY, i, j);
    			if(b > 0)
    			{
    				if(b == 1)
    				{
    					blocks[(i * 16 + j) * 128 + 39] = 44;
    				}
    				else if(b == 2)
    				{
    					blocks[(i * 16 + j) * 128 + 39] = 0;
    				}
    				else if(b == 3)
    				{
    					blocks[(i * 16 + j) * 128 + 39] = 0;
    					blocks[(i * 16 + j) * 128 + 38] = 35;
    				}
    			}
    		}
    	}
    }
	
	public int getTileBlock(int chunkX, int chunkY, int i, int j) 
	{
        if(chunkX % 5 == 0 && chunkY % 5 == 0) //ROAD2
        {
        	return ROAD_2[j * 16 + i]; 
        }
        else if(chunkX % 5 == 0) //ROAD0
        {
        	return ROAD_0[j * 16 + i]; 
        }
        else if(chunkY % 5 == 0) //ROAD1
        {
        	return ROAD_1[j * 16 + i]; 
        }
		return 0;
	}
